# ▪ 실행결과예시
# 시작값을 입력하세요.  4
# 종료값을 입력하세요.  1
# 1부터 4까지의 합계는 10입니다

시작값 = input( "시작값을 입력하세요 :")
종료값 = input( "종료값을 입력하세요 :")

시작값 = int(시작값) # 문자열을 정수로 변환
종료값 = int(종료값) # 문자열을 정수로 변환

# 시작값이 종료값보다 크다면 swap(값을 바꾸기) 해야한다.
if 시작값 > 종료값 :
    임시값 = 시작값
    시작값 = 종료값
    종료값 = 임시값
else:
    pass    

# 합계 구하는 코드
sum1 = 0
for i in range(시작값, 종료값+1, 1):
    sum1 = sum1 + i

# 출력: 1부터 4까지의 합계는 10입니다
str1 = "%s부터 %s까지의 합계는 %s입니다" % ( 시작값,종료값, sum1 )
print( str1 )