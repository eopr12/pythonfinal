# 파이썬의 내장 함수에 대해서 알아본다.
# 반올림
# 최대값
# 최소값
# 합계

value = round(1.2345)  # 반올림
print("round(1.2345)", value)  # 20

value = round(1.9876)  # 반올림
print("round(1.9876)", value)  # 2

value = max(10, 20)  # 최대값 구하기
print("max()", value)  # 20

value = min(10, 20, 30, 40, 50)  # 최소값 구하기
print("min()", value)  # 10

